package com.sitep.str.integration.in.classes;

public class Idioma {
	String idioma;

	public Idioma() {
		super();
	}

	public Idioma(String idioma) {
		super();
		this.idioma = idioma;
	}

	/**
	 * @return the idioma
	 */
	public String getIdioma() {
		return idioma;
	}

	/**
	 * @param idioma the idioma to set
	 */
	public void setIdioma(String idioma) {
		this.idioma = idioma;
	}
}
